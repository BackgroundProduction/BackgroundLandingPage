/**
 * Regenerates content/scroll-images.ts from whatever sits in
 * public/assets/scrollimages/. Runs automatically before `dev` and `build`,
 * so adding a photo to that folder is the whole workflow — no code edits.
 *
 * Ordering is by filename, so prefix with numbers (01-, 02-, …) to control
 * the flipbook sequence.
 */
import { readdirSync, writeFileSync, mkdirSync } from "node:fs";
import { join } from "node:path";

const DIR = join(process.cwd(), "public", "assets", "scrollimages");
const OUT = join(process.cwd(), "content", "scroll-images.ts");
const EXT = /\.(jpe?g|png|webp|avif|svg)$/i;

mkdirSync(DIR, { recursive: true });

const files = readdirSync(DIR)
  .filter((f) => EXT.test(f) && !f.startsWith("."))
  .sort((a, b) => a.localeCompare(b, "en", { numeric: true }));

const body = files.map((f) => `  "/assets/scrollimages/${f}",`).join("\n");

writeFileSync(
  OUT,
  `/* GENERATED by scripts/gen-scroll-images.mjs — do not edit by hand.
   Drop images into public/assets/scrollimages/ and re-run dev/build.
   ${files.length} image${files.length === 1 ? "" : "s"}. */
export const scrollImages: string[] = [
${body}
];
`
);

console.log(`[scroll-images] ${files.length} image(s) → content/scroll-images.ts`);
